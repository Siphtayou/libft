#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	n;
	int	i;

	i = 0;
	n = '\n';
	while (i < ((int)ft_strlen(s)))
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &n, 1);
}
