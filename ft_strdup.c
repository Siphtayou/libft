#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*tab;
	char	*src;
	int		i;

	i = 0;
	src = (char *)s;
	tab = malloc(ft_strlen(s) + sizeof(char));
	if (!tab)
		return (NULL);
	while (src[i])
	{
		tab[i] = src[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
